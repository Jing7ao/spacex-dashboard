def analyze(text):
    pos_hits=0;neg_hits=0;keywords=[]
    for w in POS:
        if w in text:pos_hits+=1;keywords.append(w)
    for w in NEG:
        if w in text:neg_hits+=1;keywords.append(w)
    # 情绪得分: >0.55正面, <0.45负面, 中间中性
    total=pos_hits+neg_hits
    if total==0:score=0.50
    else:score=0.5+(pos_hits-neg_hits)/total*0.5
    sentiment='positive' if score>=0.55 else 'negative' if score<=0.45 else 'neutral'
    # 股票匹配
    matched=[]
    for s in STOCKS:
        if s['n'] in text:matched.append(s['n'])
    return json.dumps({'sentiment':sentiment,'score':round(score,3),'stocks':matched,'keywords':keywords,'text':text},ensure_ascii=False)

